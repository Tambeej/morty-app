/* eslint-disable no-restricted-globals */
/**
 * Morty Service Worker
 * Implements Workbox-based caching strategies:
 * - Cache-First: Static assets (JS, CSS, images, fonts)
 * - Network-First: API calls (/api/v1/*)
 * - StaleWhileRevalidate: Navigation requests
 * - Offline fallback: /offline.html
 */

import { clientsClaim } from 'workbox-core';
import {
  ExpirationPlugin,
} from 'workbox-expiration';
import {
  precacheAndRoute,
  createHandlerBoundToURL,
} from 'workbox-precaching';
import { registerRoute } from 'workbox-routing';
import {
  StaleWhileRevalidate,
  CacheFirst,
  NetworkFirst,
} from 'workbox-strategies';
import { CacheableResponsePlugin } from 'workbox-cacheable-response';

// ─── Core Configuration ───────────────────────────────────────────────────────

// Take control of all clients immediately (skip waiting)
clientsClaim();

// Precache all assets generated by the build process
// The __WB_MANIFEST placeholder is replaced by Workbox at build time
precacheAndRoute(self.__WB_MANIFEST || []);

// ─── App Shell (SPA Navigation) ───────────────────────────────────────────────

/**
 * Handle navigation requests with the App Shell strategy.
 * All navigation requests (page loads) return the cached index.html,
 * enabling client-side routing to work offline.
 */
const fileExtensionRegexp = new RegExp('/[^/?]+\\.[^/]+$');
registerRoute(
  // Return false to exempt requests from being fulfilled by index.html.
  ({ request, url }) => {
    // If this isn't a navigation, skip.
    if (request.mode !== 'navigate') {
      return false;
    }
    // If this is a URL that starts with /_, skip.
    if (url.pathname.startsWith('/_')) {
      return false;
    }
    // If this looks like a URL for a resource, because it contains
    // a file extension, skip.
    if (url.pathname.match(fileExtensionRegexp)) {
      return false;
    }
    // Return true to signal that we want to use the handler.
    return true;
  },
  createHandlerBoundToURL(process.env.PUBLIC_URL + '/index.html')
);

// ─── Static Assets — Cache First ──────────────────────────────────────────────

/**
 * Cache JavaScript and CSS files with Cache-First strategy.
 * These files are versioned by the build tool, so they can be cached indefinitely.
 */
registerRoute(
  ({ request }) =>
    request.destination === 'script' ||
    request.destination === 'style',
  new CacheFirst({
    cacheName: 'morty-static-resources',
    plugins: [
      new CacheableResponsePlugin({
        statuses: [0, 200],
      }),
      new ExpirationPlugin({
        maxEntries: 60,
        maxAgeSeconds: 30 * 24 * 60 * 60, // 30 days
      }),
    ],
  })
);

/**
 * Cache images with Cache-First strategy.
 * Images are large and rarely change, so long-term caching is appropriate.
 */
registerRoute(
  ({ request }) => request.destination === 'image',
  new CacheFirst({
    cacheName: 'morty-images',
    plugins: [
      new CacheableResponsePlugin({
        statuses: [0, 200],
      }),
      new ExpirationPlugin({
        maxEntries: 60,
        maxAgeSeconds: 30 * 24 * 60 * 60, // 30 days
        purgeOnQuotaError: true,
      }),
    ],
  })
);

/**
 * Cache Google Fonts with Cache-First strategy.
 * Fonts are static and can be cached for a long time.
 */
registerRoute(
  ({ url }) =>
    url.origin === 'https://fonts.googleapis.com' ||
    url.origin === 'https://fonts.gstatic.com',
  new CacheFirst({
    cacheName: 'morty-google-fonts',
    plugins: [
      new CacheableResponsePlugin({
        statuses: [0, 200],
      }),
      new ExpirationPlugin({
        maxEntries: 30,
        maxAgeSeconds: 365 * 24 * 60 * 60, // 1 year
      }),
    ],
  })
);

// ─── API Calls — Network First ────────────────────────────────────────────────

/**
 * Handle API calls with Network-First strategy.
 * Always try the network first; fall back to cache if offline.
 * This ensures users see fresh data when online.
 */
registerRoute(
  ({ url }) => url.pathname.startsWith('/api/v1/'),
  new NetworkFirst({
    cacheName: 'morty-api-cache',
    networkTimeoutSeconds: 10,
    plugins: [
      new CacheableResponsePlugin({
        statuses: [0, 200],
      }),
      new ExpirationPlugin({
        maxEntries: 100,
        maxAgeSeconds: 5 * 60, // 5 minutes — short TTL for API data
      }),
    ],
  })
);

// ─── Other Resources — Stale While Revalidate ─────────────────────────────────

/**
 * Handle other requests with StaleWhileRevalidate.
 * Serve from cache immediately, then update cache in background.
 */
registerRoute(
  ({ request }) =>
    request.destination === 'document' ||
    request.destination === 'worker',
  new StaleWhileRevalidate({
    cacheName: 'morty-documents',
    plugins: [
      new CacheableResponsePlugin({
        statuses: [0, 200],
      }),
    ],
  })
);

// ─── Offline Fallback ─────────────────────────────────────────────────────────

/**
 * Catch navigation requests that fail (user is offline).
 * Return the offline fallback page.
 */
self.addEventListener('fetch', (event) => {
  if (event.request.mode === 'navigate') {
    event.respondWith(
      fetch(event.request).catch(() => {
        return caches.match('/offline.html');
      })
    );
  }
});

// ─── Background Sync ─────────────────────────────────────────────────────────

/**
 * Listen for messages from the main thread.
 * Supports SKIP_WAITING to activate new service worker immediately.
 */
self.addEventListener('message', (event) => {
  if (event.data && event.data.type === 'SKIP_WAITING') {
    self.skipWaiting();
  }
});

// ─── Push Notifications (future-ready) ───────────────────────────────────────

/**
 * Handle push notifications.
 * Currently logs the event; can be extended for mortgage analysis alerts.
 */
self.addEventListener('push', (event) => {
  if (!event.data) return;

  const data = event.data.json();
  const options = {
    body: data.body || 'ניתוח המשכנתא שלך מוכן!',
    icon: '/icons/icon-192x192.png',
    badge: '/icons/icon-72x72.png',
    vibrate: [100, 50, 100],
    data: {
      dateOfArrival: Date.now(),
      primaryKey: data.id || '1',
      url: data.url || '/dashboard',
    },
    actions: [
      {
        action: 'view',
        title: 'צפה בתוצאות',
        icon: '/icons/icon-96x96.png',
      },
      {
        action: 'close',
        title: 'סגור',
      },
    ],
  };

  event.waitUntil(
    self.registration.showNotification(
      data.title || 'Morty - ניתוח משכנתא',
      options
    )
  );
});

/**
 * Handle notification click events.
 * Opens the app and navigates to the relevant page.
 */
self.addEventListener('notificationclick', (event) => {
  event.notification.close();

  if (event.action === 'close') return;

  const urlToOpen = event.notification.data?.url || '/dashboard';

  event.waitUntil(
    clients
      .matchAll({ type: 'window', includeUncontrolled: true })
      .then((windowClients) => {
        // Check if there is already a window/tab open with the target URL
        for (let i = 0; i < windowClients.length; i++) {
          const client = windowClients[i];
          if (client.url === urlToOpen && 'focus' in client) {
            return client.focus();
          }
        }
        // If not, open a new window
        if (clients.openWindow) {
          return clients.openWindow(urlToOpen);
        }
      })
  );
});
